    private static void logYaz(String mesaj) {
        try {
            klasorleriOlustur();
            String satir = LocalDateTime.now().format(TARIH_FORMAT) + ";" + mesaj;
            Path yol = new File(logDosya).toPath();
            Files.write(yol, (satir + System.lineSeparator()).getBytes(Charset.defaultCharset()),
                    StandardOpenOption.CREATE, StandardOpenOption.APPEND);
        } catch (Exception ex) {
            logger.log(java.util.logging.Level.SEVERE, null, ex);
        }
    }

    private static String dosyadanSifreOku() {
        try {
            File f = new File(sifreDosya);
            if (!f.exists()) {
                return null;
            }
            List<String> satirlar = Files.readAllLines(f.toPath(), Charset.defaultCharset());
            if (satirlar.isEmpty()) {
                return null;
            }
            return satirlar.get(0).trim();
        } catch (Exception ex) {
            return null;
        }
    }

    private static void sifreKaydet(String sifre) {
        try {
            klasorleriOlustur();
            Path yol = new File(sifreDosya).toPath();
            Files.write(yol, sifre.getBytes(Charset.defaultCharset()),
                    StandardOpenOption.CREATE, StandardOpenOption.TRUNCATE_EXISTING);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Şifre kaydedilemedi: " + ex.getMessage());
        }
    }

    private static String sifreSor(String baslik) {
        JPasswordField pf = new JPasswordField();
        int sonuc = JOptionPane.showConfirmDialog(null, pf, baslik, JOptionPane.OK_CANCEL_OPTION);
        if (sonuc == JOptionPane.OK_OPTION) {
            return new String(pf.getPassword());
        }
        return null;
    }

    private static boolean ilkSifreBelirle() {
        String sifre1 = sifreSor("İlk kez giriş - Yeni şifre belirleyin");
        if (sifre1 == null || sifre1.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Şifre boş olamaz. Program kapanıyor.");
            return false;
        }
        String sifre2 = sifreSor("Şifreyi tekrar girin");
        if (sifre2 == null || !sifre1.equals(sifre2)) {
            JOptionPane.showMessageDialog(null, "Şifreler uyuşmadı. Program kapanıyor.");
            return false;
        }
        sifreKaydet(sifre1);
        logYaz("İlk şifre belirlendi");
        JOptionPane.showMessageDialog(null, "Şifreniz kaydedildi.");
        return true;
    }

    private static boolean sifreKontrolEt() {
        String kayitli = dosyadanSifreOku();
        if (kayitli == null || kayitli.isEmpty()) {
            return ilkSifreBelirle();
        }
        int deneme = 0;
        while (deneme < 3) {
            String girilen = sifreSor("Şifre girin (Kalan hak: " + (3 - deneme) + ")");
            if (girilen == null) {
                logYaz("Şifre girişi iptal edildi");
                return false;
            }
            if (girilen.equals(kayitli)) {
                logYaz("Giriş - şifre doğru");
                return true;
            }
            deneme++;
            logYaz("Hatalı şifre denemesi (" + deneme + "/3)");
            JOptionPane.showMessageDialog(null, "Hatalı şifre! Kalan: " + (3 - deneme));
        }
        JOptionPane.showMessageDialog(null, "3 kez hatalı giriş. Program kapanıyor.");
        logYaz("3 hatalı şifre - program sonlandı");
        return false;
    }

    private boolean sifreDogrula(String mesaj) {
        String kayitli = dosyadanSifreOku();
        if (kayitli == null) {
            return false;
        }
        String girilen = sifreSor(mesaj);
        if (girilen != null && girilen.equals(kayitli)) {
            logYaz("Şifre doğrulandı: " + mesaj);
            return true;
        }
        logYaz("Şifre doğrulanamadı: " + mesaj);
        JOptionPane.showMessageDialog(this, "Şifre hatalı!");
        return false;
    }

