    private void kelimeleriOku() {
        kelimeListesi.clear();
        try {
            File f = new File(kelimelerDosya);
            if (!f.exists()) {
                lblDurum.setText("kelimeler.txt bulunamadı!");
                return;
            }
            List<String> satirlar = Files.readAllLines(f.toPath(), Charset.defaultCharset());
            for (String s : satirlar) {
                String k = s.trim();
                if (k.length() >= 6) {
                    kelimeListesi.add(k.toUpperCase(Locale.forLanguageTag("tr")));
                }
            }
        } catch (Exception ex) {
            logger.log(java.util.logging.Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Kelimeler okunamadı: " + ex.getMessage());
        }
    }

    private void oyunaBasla() {
        if (kelimeListesi.isEmpty()) {
            kelimeleriOku();
        }
        if (kelimeListesi.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Oynanacak kelime yok. Dosyayı kontrol edin:\n" + kelimelerDosya);
            return;
        }

        oyunTimer.stop();
        yanlisSayisi = 0;
        hataliHarfler.clear();
        hataliHarfleriGuncelle();
        saniye = 0;
        oyunAktif = true;
        lblSure.setText("Süre: 0 sn");
        lblAdamResim.setIcon(null);
        txtHarf.setText("");
        txtKelime.setText("");

        int index = rnd.nextInt(kelimeListesi.size());
        seciliKelime = kelimeListesi.get(index);
        acilanHarfler = new char[seciliKelime.length()];
        for (int i = 0; i < acilanHarfler.length; i++) {
            acilanHarfler[i] = '*';
        }

        harfPanelleriniOlustur();
        lblDurum.setText("Tahmine başlayın.");
        jTabbedPane1.setSelectedIndex(0);
        oyunTimer.start();
        logYaz("Yeni oyun başlatıldı");
    }

    private void harfPanelleriniOlustur() {
        pnlKelime.removeAll();
        harfLabelleri.clear();
        for (int i = 0; i < seciliKelime.length(); i++) {
            JLabel lbl = new JLabel("*");
            lbl.setFont(new Font("Segoe UI", Font.BOLD, 28));
            harfLabelleri.add(lbl);
            pnlKelime.add(lbl);
        }
        pnlKelime.revalidate();
        pnlKelime.repaint();
    }

    private void harfleriGuncelle() {
        for (int i = 0; i < seciliKelime.length(); i++) {
            char c = acilanHarfler[i];
            harfLabelleri.get(i).setText(c == '*' ? "*" : String.valueOf(c));
        }
    }

    private boolean kelimeTamamMi() {
        for (char c : acilanHarfler) {
            if (c == '*') {
                return false;
            }
        }
        return true;
    }

    private void harfTahminYap() {
        if (!oyunAktif) {
            JOptionPane.showMessageDialog(this, "Önce oyuna başlayın.");
            return;
        }
        String giris = txtHarf.getText().trim().toUpperCase(Locale.forLanguageTag("tr"));
        if (giris.length() != 1) {
            JOptionPane.showMessageDialog(this, "Tek harf girin.");
            return;
        }
        char harf = giris.charAt(0);
        boolean bulundu = false;
        for (int i = 0; i < seciliKelime.length(); i++) {
            if (seciliKelime.charAt(i) == harf) {
                acilanHarfler[i] = harf;
                bulundu = true;
            }
        }
        txtHarf.setText("");
        logYaz("Harf tahmini: " + harf + (bulundu ? " (doğru)" : " (yanlış)"));
        if (bulundu) {
            harfleriGuncelle();
            if (kelimeTamamMi()) {
                oyunBitti(true);
            }
        } else {
            hataliHarfEkle(harf);
            yanlisTahmin();
        }
    }

    private void hataliHarfEkle(char harf) {
        for (char c : hataliHarfler) {
            if (c == harf) {
                return;
            }
        }
        hataliHarfler.add(harf);
        hataliHarfleriGuncelle();
    }

    private void hataliHarfleriGuncelle() {
        if (hataliHarfler.isEmpty()) {
            lblHataliDenemeler.setText("Hatalı denemeler: -");
            return;
        }
        StringBuilder sb = new StringBuilder("Hatalı denemeler: ");
        for (int i = 0; i < hataliHarfler.size(); i++) {
            if (i > 0) {
                sb.append(", ");
            }
            sb.append(hataliHarfler.get(i));
        }
        lblHataliDenemeler.setText(sb.toString());
    }

    private void kelimeTahminYap() {
        if (!oyunAktif) {
            JOptionPane.showMessageDialog(this, "Önce oyuna başlayın.");
            return;
        }
        String giris = txtKelime.getText().trim().toUpperCase(Locale.forLanguageTag("tr"));
        if (giris.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Kelime girin.");
            return;
        }
        txtKelime.setText("");
        logYaz("Kelime tahmini: " + giris);
        if (giris.equals(seciliKelime)) {
            for (int i = 0; i < seciliKelime.length(); i++) {
                acilanHarfler[i] = seciliKelime.charAt(i);
            }
            harfleriGuncelle();
            oyunBitti(true);
        } else {
            yanlisTahmin();
        }
    }

    private void yanlisTahmin() {
        yanlisSayisi++;
        resimGoster(yanlisSayisi);
        lblDurum.setText("Yanlış tahmin: " + yanlisSayisi + " / 11");
        if (yanlisSayisi >= 11) {
            JOptionPane.showMessageDialog(this, "11 yanlış tahmin! Kelime: " + seciliKelime);
            oyunBitti(false);
        }
    }

    private void resimGoster(int adim) {
        if (adim < 1 || adim > 11) {
            return;
        }
        String yol = resimDosyaYoluBul(adim);
        if (yol == null) {
            lblDurum.setText("Resim bulunamadı: " + resimKlasor + adim + ".jpg");
            return;
        }
        ImageIcon icon = new ImageIcon(yol);
        Image img = icon.getImage().getScaledInstance(250, 250, Image.SCALE_SMOOTH);
        lblAdamResim.setIcon(new ImageIcon(img));
        lblAdamResim.revalidate();
        lblAdamResim.repaint();
    }

    private String resimDosyaYoluBul(int adim) {
        String[] uzantilar = {".jpg", ".png"};
        for (String uz : uzantilar) {
            File f = new File(resimKlasor + adim + uz);
            if (f.isFile()) {
                return f.getAbsolutePath();
            }
        }
        return null;
    }

    private void oyunBitti(boolean kazandi) {
        oyunAktif = false;
        oyunTimer.stop();
        String sonuc = kazandi ? "Kazandı" : "Kaybetti";
        lblDurum.setText("Oyun bitti: " + sonuc + " | Kelime: " + seciliKelime);
        oyunKaydet(sonuc);
        tablolariYenile();
        JOptionPane.showMessageDialog(this,
                "Oyun Sonucu: " + (kazandi ? "Kazandı" : "Kaybetti")
                + "\nSüre: " + saniye + " sn");
        if (kazandi) {
            lblDurum.setText("Tebrikler!");
        }
    }

    private void oyunKaydet(String sonuc) {
        try {
            String satir = LocalDateTime.now().format(TARIH_FORMAT) + ";" + saniye + ";" + sonuc;
            Path yol = new File(oyunlarDosya).toPath();
            Files.write(yol, (satir + System.lineSeparator()).getBytes(Charset.defaultCharset()),
                    StandardOpenOption.CREATE, StandardOpenOption.APPEND);
            logYaz("Oyun kaydedildi: " + sonuc + " - " + saniye + " sn");
        } catch (Exception ex) {
            logger.log(java.util.logging.Level.SEVERE, null, ex);
        }
    }

    private void tablolariYenile() {
        skorTablosunuDoldur();
        logTablosunuDoldur();
    }

    private void skorTablosunuDoldur() {
        modelOyunlar.setRowCount(0);
        try {
            File f = new File(oyunlarDosya);
            if (!f.exists()) {
                return;
            }
            List<String> satirlar = Files.readAllLines(f.toPath(), Charset.defaultCharset());
            for (String satir : satirlar) {
                String[] parca = satir.split(";");
                if (parca.length >= 3) {
                    modelOyunlar.addRow(new Object[]{parca[0], parca[1], parca[2]});
                }
            }
        } catch (Exception ex) {
            logger.log(java.util.logging.Level.SEVERE, null, ex);
        }
    }

    private void logTablosunuDoldur() {
        modelLoglar.setRowCount(0);
        try {
            File f = new File(logDosya);
            if (!f.exists()) {
                return;
            }
            List<String> satirlar = Files.readAllLines(f.toPath(), Charset.defaultCharset());
            for (String satir : satirlar) {
                int nokta = satir.indexOf(';');
                if (nokta > 0) {
                    modelLoglar.addRow(new Object[]{
                        satir.substring(0, nokta),
                        satir.substring(nokta + 1)
                    });
                } else {
                    modelLoglar.addRow(new Object[]{satir, ""});
                }
            }
        } catch (Exception ex) {
            logger.log(java.util.logging.Level.SEVERE, null, ex);
        }
    }

